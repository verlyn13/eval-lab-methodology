"""Verify or render the synthetic Contract B v2 draft fixture."""

from __future__ import annotations

import argparse
from pathlib import Path

from analysis.build_contract_v2_fixture import build
from analysis.contract_v2 import (
    VerificationRefusal,
    canonical_json_bytes,
    verify_exact_bytes,
)
from analysis.contract_v2.report import render_markdown

ROOT = Path(__file__).resolve().parents[1]
DEFAULT_EVIDENCE = ROOT / "evidence" / "contract-v2" / "synthetic-not-evaluable.json"
DEFAULT_REPORT = ROOT / "evidence" / "contract-v2" / "synthetic-not-evaluable-report.md"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--evidence", type=Path, default=DEFAULT_EVIDENCE)
    parser.add_argument("--render", type=Path)
    parser.add_argument("--check", action="store_true")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    payload = args.evidence.read_bytes()
    try:
        verified = verify_exact_bytes(payload)
    except VerificationRefusal as exc:
        raise SystemExit(f"refusing: {exc}") from exc
    if args.check and args.evidence.resolve() == DEFAULT_EVIDENCE.resolve():
        regenerated = canonical_json_bytes(build())
        if payload != regenerated:
            raise SystemExit(
                "refusing: committed Contract B v2 fixture differs from regenerated bytes"
            )
    markdown = render_markdown(
        verified.human_report,
        evidence_sha256=verified.evidence_sha256,
    )
    if args.check:
        expected = DEFAULT_REPORT.read_text(encoding="utf-8")
        if markdown != expected:
            raise SystemExit(
                "refusing: committed human report does not match recomputation"
            )
    if args.render is not None:
        args.render.parent.mkdir(parents=True, exist_ok=True)
        args.render.write_text(markdown, encoding="utf-8")
    print(
        "verified Contract B v2 draft: "
        f"{verified.decision_receipt['outcome']} ({verified.evidence_sha256})"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
